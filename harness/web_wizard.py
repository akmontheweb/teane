_TEMPLATES_DIR = os.path.join(
    os.path.dirname(os.path.abspath(__file__)), "static", "templates"
)


@dataclass(frozen=True)
class StarterTemplate:
    """One entry on the 'no sessions yet — try one of these' page."""

    slug: str
    title: str
    description: str
    prompt: str
    suggested_workspace: str = ""


def load_starter_templates(*, templates_dir: Optional[str] = None) -> list[StarterTemplate]:
    """Load every ``starter-*.json`` descriptor. Sorted by title so
    the page order is deterministic."""
    root = templates_dir or _TEMPLATES_DIR
    if not os.path.isdir(root):
        return []
    out: list[StarterTemplate] = []
    for entry in sorted(os.listdir(root)):
        if not entry.startswith("starter-") or not entry.endswith(".json"):
            continue
        path = os.path.join(root, entry)
        try:
            with open(path, "r", encoding="utf-8") as f:
                raw = json.load(f)
        except (OSError, json.JSONDecodeError) as exc:
            logger.warning("[wizard] skipping bad template %s: %s", entry, exc)
            continue
        if not isinstance(raw, dict):
            continue
        slug = entry.removeprefix("starter-").removesuffix(".json")
        try:
            out.append(StarterTemplate(
                slug=slug,
                title=str(raw.get("title", slug)),
                description=str(raw.get("description", "")),
                prompt=str(raw.get("prompt", "")),
                suggested_workspace=str(raw.get("suggested_workspace", "")),
            ))
        except Exception as exc:  # noqa: BLE001
            logger.warning("[wizard] skipping malformed template %s: %s", entry, exc)
    out.sort(key=lambda t: t.title)
    return out
