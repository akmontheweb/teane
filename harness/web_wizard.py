_TEANE_HOME_DIR_ENV = "TEANE_HOME"


def _teane_home_dir() -> str:
    """Resolve the ~/.teane directory (or ``$TEANE_HOME`` override).
    Used only for the wizard's env-var sidecar; the harness itself
    still uses ``~/.harness/`` for its state (see [[project_teane_brand]])."""
    override = os.environ.get(_TEANE_HOME_DIR_ENV, "").strip()
    if override:
        return os.path.expanduser(override)
    return os.path.expanduser(os.path.join("~", ".teane"))


_ENV_KEY_SAFE = re.compile(r"^[A-Z][A-Z0-9_]*$")
_ENV_VAL_UNSAFE = re.compile(r"[\r\n\x00]")


def _validate_env_pair(name: str, value: str) -> None:
    """Refuse env-var names/values that would break a sourceable
    ``env.sh``. Names must be shell-legal; values must not contain
    control characters that could smuggle extra statements past the
    single-quote escape."""
    if not _ENV_KEY_SAFE.match(name):
        raise ValueError(f"unsafe env var name: {name!r}")
    if _ENV_VAL_UNSAFE.search(value):
        raise ValueError("env var value contains a control character")


def write_env_sh(pairs: dict[str, str], *, dest: Optional[str] = None) -> str:
    """Write a sourceable shell env file at ``~/.teane/env.sh`` (or
    ``dest``). Uses ``export VAR='value'`` with the value single-quoted
    and single quotes escaped POSIX-portably (``'\\''``).

    Overwrites existing values for the given keys; leaves any other
    exports in the file untouched so a user who has hand-tuned the
    file doesn't lose their changes.

    Sets mode 0600 (owner-read/write only) — the file may contain
    API keys.

    Returns the absolute path written.
    """
    if not pairs:
        return ""
    for name, value in pairs.items():
        _validate_env_pair(name, str(value))
    path = os.path.abspath(dest) if dest else os.path.join(_teane_home_dir(), "env.sh")
    os.makedirs(os.path.dirname(path), exist_ok=True)

    existing_lines: list[str] = []
    if os.path.isfile(path):
        try:
            with open(path, "r", encoding="utf-8") as f:
                existing_lines = f.read().splitlines()
        except OSError:
            existing_lines = []

    def _fmt(name: str, value: str) -> str:
        escaped = str(value).replace("'", "'\\''")
        return f"export {name}='{escaped}'"

    seen: set[str] = set()
    out: list[str] = []
    export_re = re.compile(r"^\s*export\s+([A-Z][A-Z0-9_]*)=")
    for line in existing_lines:
        m = export_re.match(line)
        if m and m.group(1) in pairs:
            out.append(_fmt(m.group(1), pairs[m.group(1)]))
            seen.add(m.group(1))
        else:
            out.append(line)
    # Header if this is a fresh file.
    if not existing_lines:
        out.append("# Generated by teane web wizard. `source ~/.teane/env.sh`")
        out.append("# to make API keys available to the CLI. Regenerate any time.")
    for name, value in pairs.items():
        if name in seen:
            continue
        out.append(_fmt(name, value))

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(out).rstrip("\n") + "\n")
    try:
        os.chmod(path, stat.S_IRUSR | stat.S_IWUSR)
    except OSError:
        pass
    return path


@dataclass
class ApplyResult:
    """What :func:`apply_wizard_choice` did (or refused to do)."""

    ok: bool
    config_path: str = ""
    env_sh_path: str = ""
    error: str = ""


def apply_wizard_choice(
    *,
    provider: str,
    api_key: str,
    dashboard_cfg: Any,
    dest_config_path: Optional[str] = None,
    dest_env_sh_path: Optional[str] = None,
    validate: bool = True,
) -> ApplyResult:
    """Write a fresh ``config.json`` for ``provider`` and (for remote
    providers) persist ``api_key`` to ``~/.teane/env.sh``.

    Does NOT put the key inside ``config.json``. The harness's config
    validation resolves secrets from env vars; keeping them in a
    sidecar means the config can be checked into a private repo
    without leaking credentials.

    Validation is optional so tests can exercise the write path
    without importing the full harness config validator; production
    callers should leave it on.
    """
    from harness.cli import _get_global_config_path  # local import

    provider = (provider or "").strip().lower()
    if provider not in DEFAULT_MODELS_BY_PROVIDER:
        return ApplyResult(
            ok=False,
            error=(
                f"unknown provider {provider!r}; "
                f"pick one of {sorted(DEFAULT_MODELS_BY_PROVIDER)}"
            ),
        )
    if provider not in _LOCAL_PROVIDERS and not (api_key or "").strip():
        return ApplyResult(
            ok=False,
            error=f"{PROVIDER_ENV_VAR[provider]} is required for {provider}",
        )

    model_key = DEFAULT_MODELS_BY_PROVIDER[provider]
    config = build_default_config(provider, model_key)

    # Persist the API key first — the config validator will insist the
    # key be resolvable when it runs.
    env_sh_path = ""
    if provider not in _LOCAL_PROVIDERS:
        env_var = PROVIDER_ENV_VAR[provider]
        os.environ[env_var] = api_key
        try:
            env_sh_path = write_env_sh(
                {env_var: api_key},
                dest=dest_env_sh_path,
            )
        except ValueError as exc:
            return ApplyResult(ok=False, error=str(exc))
        except OSError as exc:
            return ApplyResult(
                ok=False,
                error=f"could not write env sidecar: {exc}",
            )

    config_path = os.path.abspath(dest_config_path) if dest_config_path else _get_global_config_path()
    os.makedirs(os.path.dirname(config_path), exist_ok=True)
    tmp = config_path + ".tmp"
    try:
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(config, f, indent=2, sort_keys=False)
            f.write("\n")
        os.replace(tmp, config_path)
    except OSError as exc:
        return ApplyResult(ok=False, error=f"config write failed: {exc}")

    if validate:
        try:
            from harness.cli import validate_config_strict
            validate_config_strict(config, source=config_path)
        except Exception as exc:  # noqa: BLE001
            return ApplyResult(
                ok=False,
                config_path=config_path,
                env_sh_path=env_sh_path,
                error=f"generated config failed strict validation: {exc}",
            )

    return ApplyResult(
        ok=True,
        config_path=config_path,
        env_sh_path=env_sh_path,
    )


