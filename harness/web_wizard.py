"""Web-first onboarding wizard.

Phase 3 of the consumer-grade UI overhaul (see plan file
``~/.claude/plans/make-a-plan-for-staged-quasar.md``).

This module owns:

- **State detection** — :func:`wizard_state` walks the current install
  and reports which of three states the /home page should render:
    1. ``needs_setup``    — no ``config/config.json`` yet, or the
                            config's routed models reference API keys
                            that aren't set.
    2. ``no_sessions``    — config OK but no runs have ever landed
                            here. Shows "what do you want to build?"
                            + starter template cards.
    3. ``has_sessions``   — config OK, at least one run in
                            ``log_dir``. Shows the same "build"
                            prompt plus a resume panel.
- **Setup application** — :func:`apply_wizard_choice` writes a
  minimal ``config.json`` from a chosen provider and (optionally)
  persists the API key to ``~/.teane/env.sh`` so future shells
  source it. It never puts secrets inside ``config.json`` — the
  harness architecture already separates secrets via env vars.
- **Starter templates** — :func:`load_starter_templates` reads the
  three JSON descriptors under ``harness/static/templates/`` that
  drive the "no sessions yet — try one of these" cards.

Deliberately no HTTP concerns here. The HTTP handlers live in
:mod:`harness.dashboard`; this module is a plain library so the CLI
wizard could adopt the same building blocks later without pulling in
the dashboard server.
"""

