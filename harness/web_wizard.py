# State detection
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class WizardState:
    """The three-way rendering signal for the Home page.

    ``kind`` is one of ``needs_setup`` / ``no_sessions`` / ``has_sessions``.
    All other fields are informational — they let the renderer show
    contextual help (e.g. which providers already have a key set).
    """

    kind: str
    config_path: str
    has_config: bool
    missing_env_vars: tuple[str, ...] = ()
    providers_with_key_set: tuple[str, ...] = ()
    session_count: int = 0
    log_dir: str = ""


def _providers_with_key_set() -> tuple[str, ...]:
    """Which of the four known providers currently have their env var
    populated in this process. Used to prefer the already-configured
    provider when auto-picking the wizard's default choice."""
    have: list[str] = []
    for provider, env_var in PROVIDER_ENV_VAR.items():
        if not env_var:  # local provider, always "available"
            have.append(provider)
            continue
        if os.environ.get(env_var, "").strip():
            have.append(provider)
    return tuple(have)


def wizard_state(dashboard_cfg: Any) -> WizardState:
    """Compute the state the Home page should render.

    Never raises. If any subsystem probe fails we degrade to
    ``needs_setup`` so the page prompts the operator to fix things,
    rather than crashing before they see the wizard.
    """
    # Import lazily so a broken cli import doesn't take down the
    # whole dashboard module at load time.
    try:
        from harness.cli import (  # type: ignore
            _get_global_config_path,
            discover_config,
            find_missing_api_keys,
        )
    except Exception as exc:  # noqa: BLE001
        logger.warning("[wizard] cli import failed: %s", exc)
        return WizardState(
            kind="needs_setup",
            config_path="",
            has_config=False,
            providers_with_key_set=_providers_with_key_set(),
        )

    config_path = _get_global_config_path()
    log_dir = os.path.expanduser(getattr(dashboard_cfg, "log_dir", "") or "")

    has_config = os.path.isfile(config_path)
    missing: dict[str, list[str]] = {}
    if has_config:
        try:
            cfg_dict = discover_config(None)
            missing = find_missing_api_keys(cfg_dict) or {}
        except Exception as exc:  # noqa: BLE001
            # Malformed config or missing required fields — treat as
            # needing setup so the wizard shows.
            logger.info("[wizard] config load failed, needs setup: %s", exc)
            return WizardState(
                kind="needs_setup",
                config_path=config_path,
                has_config=False,
                providers_with_key_set=_providers_with_key_set(),
            )

    session_count = 0
    if log_dir and os.path.isdir(log_dir):
        try:
            from harness.metrics import list_sessions
            session_count = len(list_sessions(log_dir))
        except Exception as exc:  # noqa: BLE001
            logger.warning("[wizard] list_sessions failed: %s", exc)

    missing_env = tuple(sorted(missing.keys()))
    if not has_config or missing_env:
        return WizardState(
            kind="needs_setup",
            config_path=config_path,
            has_config=has_config,
            missing_env_vars=missing_env,
            providers_with_key_set=_providers_with_key_set(),
            session_count=session_count,
            log_dir=log_dir,
        )

    kind = "has_sessions" if session_count > 0 else "no_sessions"
    return WizardState(
        kind=kind,
        config_path=config_path,
        has_config=True,
        providers_with_key_set=_providers_with_key_set(),
        session_count=session_count,
        log_dir=log_dir,
    )


